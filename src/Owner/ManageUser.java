    private void displayCustomerData(){
        PreparedStatement pst;
        ResultSet rs;
        try {
            pst = con.prepareStatement("SELECT * FROM user WHERE level=?");
            pst.setString(1, "Customer");
            rs = pst.executeQuery();

            // Creating a DefaultTableModel to hold the data for the JTable
            DefaultTableModel model = (DefaultTableModel) tbCustomer.getModel();

            // Clear the existing data from the table
            model.setRowCount(0);

            while (rs.next()) {
                int id = rs.getInt("id_user");
                String name = rs.getString("firstname") + " " + rs.getString("lastname");
                String username = rs.getString("username");
                String phoneNumber = rs.getString("telp");
                String address = rs.getString("alamat");

                // Add the data to the table model
                Vector<Object> row = new Vector<>();
                row.add(id);
                row.add(name);
                row.add(username);
                row.add(phoneNumber);
                row.add(address);

                model.addRow(row);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error fetching services data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
