    private void btnDeleteServicesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteServicesActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbServices.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Tolong pilih baris yang ingin di hapus.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int serviceId = (int) tbServices.getValueAt(selectedRow, 0);

        // Konfirmasi penghapusan data
        int option = JOptionPane.showConfirmDialog(this, "Anda yakin ingin menghapus layanan ini?", "Delete Order", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION) {
            try {
                // Hapus data dari database
                PreparedStatement pstDelete = con.prepareStatement("DELETE FROM `services` WHERE id_services= ?");
                pstDelete.setInt(1, serviceId);
                int rowsAffected = pstDelete.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Layanan berhasil dihapus.", "Success", JOptionPane.INFORMATION_MESSAGE);
                    displayServicesData(); // Refresh tampilan tabel setelah penghapusan
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus Layanan.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Error deleting order: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnDeleteServicesActionPerformed

