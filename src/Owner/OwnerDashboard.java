    private void displayServicesData(){
        try {
            String query = "SELECT * FROM services";
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();

            // Creating a DefaultTableModel to hold the data for the JTable
            DefaultTableModel model = (DefaultTableModel) tbServices.getModel();

            // Clear the existing data from the table
            model.setRowCount(0);

            while (rs.next()) {
                // Retrieve data from the ResultSet
                int id = rs.getInt("id_services");
                String serviceName = rs.getString("jenis");
                double servicePrice = rs.getDouble("harga");

                // Add the data to the table model
                Vector<Object> row = new Vector<>();
                row.add(id);
                row.add(serviceName);
                row.add(servicePrice);

                model.addRow(row);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error fetching services data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void countOrderByStatus(){
        try {
            String queryCustomer = "SELECT COUNT(*) FROM `user` WHERE level = 'Customer'";
            String queryWorker = "SELECT COUNT(*) FROM `user` WHERE level = 'Petugas'";
            String queryOrder = "SELECT COUNT(*) FROM `order`";

            // Menghitung jumlah order untuk setiap status
            int customerCount = getCountByQuery(queryCustomer);
            int workerCount = getCountByQuery(queryWorker);
            int orderCount = getCountByQuery(queryOrder);

            // Memperbarui label
            lblCustomerCount.setText(String.valueOf(customerCount));
            lblWorkerCount.setText(String.valueOf(workerCount));
            lblOrderCount.setText(String.valueOf(orderCount));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error counting orders by status: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private int getCountByQuery(String query) throws SQLException {
        int count = 0;
        pst = con.prepareStatement(query);
        rs = pst.executeQuery();
        if (rs.next()) {
            count = rs.getInt(1);
        }
        return count;
    }
    
