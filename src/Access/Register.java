    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String firstname = tfFirstname.getText();
        String lastname = tfLastname.getText();
        String username = tfUsername.getText();
        String password = tfPassword.getText();
        String confirmPassword = tfConfirmPassword.getText();
        String phone = tfPhone.getText();
        String address = tfAddress.getText();
        
        if(firstname.isEmpty() || lastname.isEmpty() || username.isEmpty() || password.isEmpty() ||
               confirmPassword.isEmpty() || phone.isEmpty() || address.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please enter all fields", "Try Again", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Confirm Password doesn't match", "Try Again", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            String sql = "SELECT username FROM user WHERE username=?";
            pst = con.prepareStatement(sql);
            pst.setString(1, username);
            rs = pst.executeQuery();

            if(rs.next()) {
                JOptionPane.showMessageDialog(this, "Username Already Taken", "Try Again", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch(Exception ex) {
            System.out.println("Error " + ex);
        }
        
        addUserToDatabase(firstname, lastname, username, password, phone, address);
    }//GEN-LAST:event_btnRegisterActionPerformed

