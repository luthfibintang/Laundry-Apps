    private void addUserToDatabase(String firstName, String lastName, String username, String password, String phone, String address){
         try{
                pst = con.prepareStatement("INSERT INTO user (firstname, lastname, username, password, telp, alamat, level) VALUE(?,?,?,?,?,?,?)");
                pst.setString(1, firstName);
                pst.setString(2, lastName);
                pst.setString(3, username);
                pst.setString(4, password);
                pst.setString(5, phone);
                pst.setString(6, address);
                pst.setString(7, "Customer");
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Register Success", "Success", JOptionPane.INFORMATION_MESSAGE);
                LoginPage login = new LoginPage();
                login.setVisible(true);
                this.dispose(); 
            } catch(SQLException ex){
                System.out.println("" + ex);
            }
    }

