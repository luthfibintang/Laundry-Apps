    private void displayServicesData(){
        try {
            String query = "SELECT * FROM services";
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();

            // Creating a DefaultTableModel to hold the data for the JTable
            DefaultTableModel model = (DefaultTableModel) tbServices.getModel();

            // Clear the existing data from the table
            model.setRowCount(0);

            while (rs.next()) {
                // Retrieve data from the ResultSet
                String serviceName = rs.getString("jenis");
                double servicePrice = rs.getDouble("harga");

                // Add the data to the table model
                Vector<Object> row = new Vector<>();
                row.add(serviceName);
                row.add(servicePrice);

                model.addRow(row);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error fetching services data: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
