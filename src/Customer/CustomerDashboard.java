    private void countOrderByStatus(){
        try {
            String queryUnconfirm = "SELECT COUNT(*) FROM `order` WHERE id_user=? AND status = 'Menunggu Konfirmasi'";
            String queryInProgress = "SELECT COUNT(*) FROM `order` WHERE id_user=? AND status != 'Menunggu Konfirmasi' AND status != 'Selesai' AND status !='Ditolak'";
            String queryFinish = "SELECT COUNT(*) FROM `order` WHERE id_user=? AND status = 'Selesai'";

            // Menghitung jumlah order untuk setiap status
            int unconfirmCount = getCountByQuery(queryUnconfirm);
            int inProgressCount = getCountByQuery(queryInProgress);
            int finishCount = getCountByQuery(queryFinish);

            // Memperbarui label
            lblUnconfirm.setText(String.valueOf(unconfirmCount));
            lblInProgress.setText(String.valueOf(inProgressCount));
            lblFinish.setText(String.valueOf(finishCount));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error counting orders by status: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private int getCountByQuery(String query) throws SQLException {
        int count = 0;
        pst = con.prepareStatement(query);
        pst.setInt(1, getIdUser(username));
        rs = pst.executeQuery();
        if (rs.next()) {
            count = rs.getInt(1);
        }
        return count;
    }
    
    private int getIdUser(String username) throws SQLException {
        PreparedStatement pstIdUser;

        ResultSet rs;
        pstIdUser = con.prepareStatement("SELECT id_user FROM user WHERE username = ?");
        pstIdUser.setString(1, username);
        rs = pstIdUser.executeQuery();
        if(rs.next()){
            return rs.getInt("id_user");
        }
        return 0;
    }

